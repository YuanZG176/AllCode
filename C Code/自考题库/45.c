

//45、假定int 类型变量占用两个字节，其有定义：int  x[10]={0,2,4};,则数组x在内存中所占字节数是___________。
//A、3
//B、6
//C、10
//D、20
//正确答案：D





//在C语言中，数组x的大小由其定义时指定的元素数量决定，而不是由初始化列表中的元素数量决定。
//题目中定义了一个包含10个整数（int类型）的数组x，即使只初始化了前3个元素，
//剩余的元素会被自动初始化为0（对于全局或静态变量）或者保持未定义值（对于局部变量），但它们仍然占用内存空间。
//
//既然题目指出int类型变量占用两个字节，那么每个int类型的元素占用2个字节。
//因此，一个包含10个int类型元素的数组将占用 10×2=2010×2=20 个字节。
